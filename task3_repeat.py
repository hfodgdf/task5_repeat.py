# Завдання 5: Сума всіх цілих чисел між двома числами включно

# Введення чисел користувачем
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))

# Обчислення суми всіх цілих чисел між a і b включно
total = sum(range(min(a, b), max(a, b) + 1))

# Вивід результату
print(f"Сума всіх чисел від {a} до {b} включно: {total}")
