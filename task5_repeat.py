def повторити(функція, разів, значення):
    результат = значення
    for _ in range(разів):
        результат = функція(результат)
    return результат


if __name__ == "__main__":
    print(повторити(lambda x: x + 1, 3, 5))
    print(повторити(lambda x: x * 3, 3, 2))
    print(повторити(lambda x: x ** 2, 2, 3))
