# Створіть функцію, яка приймає функцію як аргумент і виконує її кілька разів поспіль із одним і тим самим значенням.
# Наприклад, repeat(lambda x:x+1, 3, 5) має повернути результат застосування функції x+1 три рази до числа 5 (тобто 8).

def repeat(func, times, value):
    """
    Виконує передану функцію 'func' кілька разів поспіль з початковим значенням 'value'.
    :param func: функція, яку потрібно застосувати
    :param times: кількість повторів
    :param value: початкове значення
    :return: результат після багаторазового застосування функції
    """
    result = value
    for _ in range(times):
        result = func(result)
    return result


# --- Приклади використання ---
if __name__ == "__main__":
    # 1️⃣ Простий приклад: додаємо 1 три рази до 5
    print(repeat(lambda x: x + 1, 3, 5))   # Результат: 8

    # 2️⃣ Множимо число 2 на 3 три рази
    print(repeat(lambda x: x * 3, 3, 2))   # Результат: 54

    # 3️⃣ Підносимо до квадрату двічі
    print(repeat(lambda x: x ** 2, 2, 3))  # Результат: 81
